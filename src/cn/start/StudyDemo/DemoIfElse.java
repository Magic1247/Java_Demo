package cn.start.StudyDemo;

public class DemoIfElse {
	public static void main(String[] args){
		ifelse();
	}
	public static void ifelse(){
		byte num = 1;
		if(num%2 == 0){
			System.out.println(""+ num%2);
			System.out.println("");
		}
		else{
			System.out.println(""+ num%2);
			System.out.println("");
		}
	}
}