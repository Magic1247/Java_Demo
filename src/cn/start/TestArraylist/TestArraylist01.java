package cn.start.TestArraylist;

public class TestArraylist01 {
    private String name;
    private int age;

    public TestArraylist01() {
    }

    public TestArraylist01(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
