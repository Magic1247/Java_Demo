package cn.start.TestMath;

public class TestMath01 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-0.25)); // 取绝对值
        System.out.println(Math.ceil(3.14)); // 向上取整
        System.out.println(Math.floor(3.14)); // 向下取整
        System.out.println(Math.round(3.4)); // 四舍五入
        System.out.println(Math.round(3.5)); // 四舍五入
        System.out.println(Math.PI);

    }
}
